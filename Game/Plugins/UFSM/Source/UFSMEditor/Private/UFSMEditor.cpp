UFSMFactory::UFSMFactory(const class FObjectInitializer& OBJ) : Super(OBJ) {
	SupportedClass = UStateMachineComponent::StaticClass();
	bEditAfterNew = true;
	bCreateNew = true;
}

UObject* UFSMFactory::FactoryCreateNew(UClass* Class, UObject* InParent, FName Name, EObjectFlags Flags, UObject* Context, FFeedbackContext* Warn) {
	check(Class->IsChildOf(UStateMachineComponent::StaticClass()));
	return FKismetEditorUtilities::CreateBlueprint(Class,InParent,Name,BPTYPE_Normal,UBlueprint::StaticClass(),UBlueprintGeneratedClass::StaticClass(),TEXT("AssetTypeActions"));
}

FText FATA_FSM::GetAssetDescription(const FAssetData& AssetData) const {
	return FText::FromString(FString(TEXT("FSM: Blueprintable Finite State Machine Component.")));
}

